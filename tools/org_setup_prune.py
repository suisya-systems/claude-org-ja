def extract_role_blocks(md_text: str, roles: dict) -> dict[str, dict | None]:
    """Mirror of check_role_configs.extract_role_blocks (kept inline to avoid a
    cross-module import at script invocation time)."""
    results: dict[str, dict | None] = {}
    sections = re.split(r"(?m)^## ", md_text)
    for role_name, role_def in roles.items():
        marker = role_def.get("docs_section")
        if not marker:
            continue
        block = None
        for section in sections[1:]:
            if marker in section.splitlines()[0]:
                m = re.search(r"```json\n(.*?)\n```", section, re.DOTALL)
                if m:
                    block = json.loads(m.group(1))
                break
        results[role_name] = block
    return results


