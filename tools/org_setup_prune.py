#!/usr/bin/env python3
"""org-setup --prune: rewrite role settings.local.json from permissions.md.

Background (Issue #88): the additive ``/org-setup`` cannot remove stale or
broadened entries from ``settings.local.json``. This tool rewrites the file
from the role template embedded in
``.claude/skills/org-setup/references/permissions.md`` (the human-readable
projection of ``tools/role_configs_schema.json``) so drift is fully resolved.

User-extension protection (Issue #88 option A): if a sibling
``settings.local.override.json`` exists, its contents are deep-merged on top
of the template. Users put bespoke entries there once; the prune tool never
touches it, so personal customizations survive every rewrite.

Targets: secretary / dispatcher / curator. (worker is generated by
org-delegate -- out of scope here.)

``user_common`` (``~/.claude/settings.json``) lives in the user's home and is
shared with non-org plugins, so the prune rewrite would clobber unrelated
keys. Instead a dedicated mode -- ``--user-common-sandbox`` -- performs only
a *targeted, idempotent union merge* into the user-level sandbox block:

- ``sandbox.filesystem.denyRead`` for sensitive credential directories
  (existence-checked + realpath-escape skipped on WSL; ``~``-prefixed
  literal entries preserved so the file stays portable across users).
- ``sandbox.filesystem.denyWrite`` for ``~/.claude/settings.json``
  (file literal, NOT existence-checked -- preventive deny so the write
  guard is in place before Claude Code first creates the file).

All other keys are passed through untouched. See Issue #429 for the
denyRead migration (Task B/C) and Issue #433 for the denyWrite migration
out of the shared, checked-in ``.claude/settings.json``.

Usage:
    py -3 tools/org_setup_prune.py --role secretary --dry-run
    py -3 tools/org_setup_prune.py --role dispatcher
    py -3 tools/org_setup_prune.py --all --dry-run
    py -3 tools/org_setup_prune.py --user-common-sandbox --dry-run
    py -3 tools/org_setup_prune.py --user-common-sandbox
"""

